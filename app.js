const trackList = document.querySelector("#trackList");
const searchInput = document.querySelector("#searchInput");
const counter = document.querySelector("#counter");
const trackNumber = document.querySelector("#trackNumber");
const trackTitle = document.querySelector("#trackTitle");
const trackArtist = document.querySelector("#trackArtist");
const storyTitle = document.querySelector("#storyTitle");
const storyText = document.querySelector("#storyText");
const audioPlayer = document.querySelector("#audioPlayer");
const audioNote = document.querySelector("#audioNote");
const youtubeLink = document.querySelector("#youtubeLink");
const spotifyLink = document.querySelector("#spotifyLink");
const copyButton = document.querySelector("#copyButton");
const themeButtons = document.querySelectorAll(".theme-button");
const playButton = document.querySelector("#playButton");
const progressBar = document.querySelector("#progressBar");
const progressFill = document.querySelector("#progressFill");
const jellyfishMarker = document.querySelector("#jellyfishMarker");
const currentTimeLabel = document.querySelector("#currentTime");
const durationTimeLabel = document.querySelector("#durationTime");
const contentTabs = document.querySelectorAll(".content-tab");

let selectedIndex = 0;
let activeContentView = "story";
const savedTheme = getSavedTheme();

function getSavedTheme() {
  try {
    return localStorage.getItem("playlist-theme") || "rose";
  } catch {
    return "rose";
  }
}

function applyTheme(theme) {
  document.body.classList.toggle("theme-blue", theme === "blue");
  themeButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.theme === theme);
  });
  try {
    localStorage.setItem("playlist-theme", theme);
  } catch {
    // The selected theme still applies for the current session.
  }
}

function normalize(value) {
  return value.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
}

function searchUrl(base, track) {
  return `${base}${encodeURIComponent(`${track.title} ${track.artist}`)}`;
}

function escapeHtml(value) {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function formatTime(seconds) {
  if (!Number.isFinite(seconds)) {
    return "0:00";
  }

  const minutes = Math.floor(seconds / 60);
  const remainingSeconds = Math.floor(seconds % 60).toString().padStart(2, "0");
  return `${minutes}:${remainingSeconds}`;
}

function updateProgress() {
  const duration = audioPlayer.duration || 0;
  const percent = duration ? (audioPlayer.currentTime / duration) * 100 : 0;
  const progress = `${Math.min(100, Math.max(0, percent))}%`;

  progressBar.style.setProperty("--progress", progress);
  progressFill.style.setProperty("--progress", progress);
  jellyfishMarker.style.setProperty("--progress", progress);
  currentTimeLabel.textContent = formatTime(audioPlayer.currentTime);
  durationTimeLabel.textContent = formatTime(duration);
}

function updatePlayButton() {
  playButton.textContent = audioPlayer.paused ? "▶" : "Ⅱ";
  playButton.setAttribute("aria-label", audioPlayer.paused ? "Reproducir" : "Pausar");
}

function renderStoryBlock(block) {
  const text = escapeHtml(block.text);

  if (block.type === "lyrics") {
    return `<blockquote class="lyric-block">${text}</blockquote>`;
  }

  return `<p>${text}</p>`;
}

function renderLyrics(track) {
  if (!track.lyrics || !track.lyrics.trim()) {
    return `
      <p class="lyrics-empty">
        Letra pendiente. Para añadirla, busca esta canción en app.js y pega el texto dentro de <code>lyrics: \`...\`</code>.
      </p>
    `;
  }

  return `<pre class="full-lyrics">${escapeHtml(track.lyrics)}</pre>`;
}

function setContentView(view) {
  activeContentView = view;
  contentTabs.forEach((tab) => {
    tab.classList.toggle("active", tab.dataset.view === view);
  });
  renderSelectedContent();
}

function renderSelectedContent() {
  const track = tracks[selectedIndex];
  storyText.innerHTML = activeContentView === "lyrics"
    ? renderLyrics(track)
    : track.story.map(renderStoryBlock).join("");
}

function storyAsText(track) {
  return `${track.story.map((block) => block.text).join(" ")} ${track.lyrics || ""}`;
}

function renderList() {
  const query = normalize(searchInput.value.trim());
  const visibleTracks = tracks
    .map((track, index) => ({ track, index }))
    .filter(({ track }) => {
      const haystack = normalize(`${track.title} ${track.artist} ${storyAsText(track)}`);
      return haystack.includes(query);
    });

  trackList.innerHTML = "";
  counter.textContent = `${visibleTracks.length} canciones`;

  visibleTracks.forEach(({ track, index }) => {
    const button = document.createElement("button");
    button.className = `track-button${index === selectedIndex ? " active" : ""}`;
    button.type = "button";
    button.innerHTML = `
      <span class="track-index">${index + 1}</span>
      <span>
        <span class="track-name">${escapeHtml(track.title)}</span>
        <span class="track-artist">${escapeHtml(track.artist)}</span>
      </span>
    `;
    button.addEventListener("click", () => selectTrack(index));
    trackList.appendChild(button);
  });
}

function selectTrack(index) {
  selectedIndex = index;
  const track = tracks[index];
  const label = `${track.title} - ${track.artist}`;

  trackNumber.textContent = `Canción ${index + 1}`;
  trackTitle.textContent = track.title;
  trackArtist.textContent = track.artist;
  storyTitle.textContent = label;
  renderSelectedContent();

  audioPlayer.src = track.file;
  audioPlayer.load();
  updateProgress();
  updatePlayButton();
  audioNote.textContent = "";
  youtubeLink.href = track.youtube || searchUrl("https://www.youtube.com/results?search_query=", track);
  spotifyLink.href = track.spotify || searchUrl("https://open.spotify.com/search/", track);
  if (copyButton) {
    copyButton.dataset.copy = label;
  }

  renderList();
}

audioPlayer.addEventListener("error", () => {
  const track = tracks[selectedIndex];
  audioNote.textContent = `No encuentro ${track.file}. Puedes crear la carpeta audio y añadir el MP3 con ese nombre, o escucharla con los botones.`;
});

audioPlayer.addEventListener("loadedmetadata", updateProgress);
audioPlayer.addEventListener("timeupdate", updateProgress);
audioPlayer.addEventListener("play", updatePlayButton);
audioPlayer.addEventListener("pause", updatePlayButton);
audioPlayer.addEventListener("ended", updatePlayButton);

playButton.addEventListener("click", async () => {
  if (audioPlayer.paused) {
    try {
      await audioPlayer.play();
    } catch {
      audioNote.textContent = "No se pudo iniciar la reproducción. Prueba a seleccionar la canción de nuevo.";
    }
    return;
  }

  audioPlayer.pause();
});

progressBar.addEventListener("click", (event) => {
  if (!audioPlayer.duration) {
    return;
  }

  const rect = progressBar.getBoundingClientRect();
  const ratio = (event.clientX - rect.left) / rect.width;
  audioPlayer.currentTime = Math.min(audioPlayer.duration, Math.max(0, ratio * audioPlayer.duration));
  updateProgress();
});

searchInput.addEventListener("input", renderList);

if (copyButton) {
  copyButton.addEventListener("click", async () => {
    try {
      await navigator.clipboard.writeText(copyButton.dataset.copy);
      copyButton.textContent = "Copiado";
      window.setTimeout(() => {
        copyButton.textContent = "Copiar título";
      }, 1200);
    } catch {
      copyButton.textContent = "No se pudo copiar";
    }
  });
}

themeButtons.forEach((button) => {
  button.addEventListener("click", () => applyTheme(button.dataset.theme));
});

contentTabs.forEach((tab) => {
  tab.addEventListener("click", () => setContentView(tab.dataset.view));
});

applyTheme(savedTheme);
selectTrack(0);


